#include <stdio.h>

int main()
{
	int x;

	for (x = 0; x < 100; ++x) {
		if (x % 10 != 5 && x % 10 != 8) {
			printf("%2i ", x);
			if (x % 10 == 9)
			{
				printf("\n");
			}
		}
	}
	return 0;
}

